/**
 * 流量相关语义色（区分深浅色）
 */
object TrafficColors {
    @Composable
    fun rxColor(darkTheme: Boolean = isSystemInDarkTheme()) =
        if (darkTheme) traffic_rx_color_dark else traffic_rx_color_light

    @Composable
    fun txColor(darkTheme: Boolean = isSystemInDarkTheme()) =
        if (darkTheme) traffic_tx_color_dark else traffic_tx_color_light

    @Composable
    fun blockedColor(darkTheme: Boolean = isSystemInDarkTheme()) =
        if (darkTheme) traffic_blocked_color_dark else traffic_blocked_color_light

    @Composable
    fun warningColor(darkTheme: Boolean = isSystemInDarkTheme()) =
        if (darkTheme) traffic_warning_color_dark else traffic_warning_color_light
}

/**
 * 全局主题入口
 * @param darkTheme 是否使用深色主题
 * @param dynamicColor 是否使用Android 12+动态取色（默认关闭，使用固定蓝色盾牌主题）
 * @param content 主题包裹的内容
 */
@Composable
fun LiuYuGuardTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    dynamicColor: Boolean = false, // 固定蓝色盾牌主题，不跟随壁纸取色
    content: @Composable () -> Unit
) {
    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context)
            else dynamicLightColorScheme(context)
        }
        darkTheme -> DarkColorScheme
        else -> LightColorScheme
    }

    val view = LocalView.current
    if (!view.isInEditMode) {
        SideEffect {
            val window = (view.context as Activity).window
            window.statusBarColor = colorScheme.primary.toArgb()
            WindowCompat.getInsetsController(window, view).isAppearanceLightStatusBars = !darkTheme
        }
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        content = content
    )
}